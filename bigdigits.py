# tool transform provided digits to huge ones
# run this tool with int argument

import sys
input = sys.argv[1]

numbers = [[' *** ','*   *','*   *','*   *','*   *','*   *',' *** '],
           [' * ','** ',' * ',' * ',' * ',' * ','***'],
           [' *** ','*   *','*   *','  *  ',' *   ','*    ','*****'],
           [' *** ','*   *','    *','  ** ','    *','*   *',' *** '],
           ['   * ','  ** ',' * * ','*  * ','*****','   * ','   * '],
           ['*****','*    ','*    ','**** ','    *','*   *',' *** '],
           [' *** ','*    ','*    ','**** ','*   *','*   *',' *** '],
           ['*****','    *','   * ','  *  ',' *   ','*    ','*    '],
           [' *** ','*   *','*   *',' *** ','*   *','*   *',' *** '],
           [' ****','*   *','*   *',' ****','    *','    *','    *']]

for j in range(7):
    row = ''
    for i in input:
        pre_row = numbers[int(i)][j]
        for k in pre_row:
            if k == ' ':
                row += k
            else:
                row += i
        row += ' '
    print(row)